public class Bai02 {
    public static void main(String[] args) {
        int a = 21;
        int b = 20;

        if (a > b) {
            System.out.println("a lon hon b");
        } else if (a == b) {
            System.out.println("a bang b");
        } else {
            System.out.println("a nho hon b");
        }
    }
}
