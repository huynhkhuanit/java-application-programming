package Ly_Thuyet.conditionals;

public class checkNegativePositiveNumber {
    public static void main(String[] args) {
        int number = -10;
        if (number > 0) {
            System.out.println("Number " + number + " is positive!");
        }
        else {
            System.out.println("Number " + number + " is negative!");
        }
    }
}